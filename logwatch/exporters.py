def _h_table(
    headers: Sequence[str],
    rows: Sequence[Sequence[object]],
    *,
    num_cols: Sequence[int] = (),
    mono_cols: Sequence[int] = (),
) -> str:
    """Render an HTML table with every cell escaped. Empty rows -> a placeholder."""
    if not rows:
        return '<p class="empty">(none)</p>'
    thead = "".join(f"<th>{escape(str(h))}</th>" for h in headers)
    body_rows = []
    for row in rows:
        cells = []
        for i, cell in enumerate(row):
            cls = []
            if i in num_cols:
                cls.append("num")
            if i in mono_cols:
                cls.append("mono")
            attr = f' class="{" ".join(cls)}"' if cls else ""
            cells.append(f"<td{attr}>{escape(str(cell))}</td>")
        body_rows.append("<tr>" + "".join(cells) + "</tr>")
    return (
        "<table><thead><tr>"
        + thead
        + "</tr></thead><tbody>"
        + "".join(body_rows)
        + "</tbody></table>"
    )


def _h_disclaimer() -> str:
    return f'<p class="disclaimer">{escape(ASN_DISCLAIMER)}</p>'


def render_html(data: Dict, top_n: int) -> str:
    """Build a complete, self-contained HTML document from a result dict."""
    if data.get("kind") == "auth":
        body = _html_auth_body(data, top_n)
        title = "SSH auth log report"
    else:
        body = _html_access_body(data, top_n)
        title = "HTTP access log report"

    risk = escape(str(data.get("risk", "LOW")))
    parts = [
        "<!DOCTYPE html>",
        '<html lang="en">',
        "<head>",
        '<meta charset="utf-8">',
        '<meta name="viewport" content="width=device-width, initial-scale=1">',
        f"<title>logwatch - {escape(title)}</title>",
        f"<style>{_HTML_CSS}</style>",
        "</head>",
        "<body><div class='wrap'>",
        f"<h1>{escape(title)}</h1>",
        f'<p class="meta">Generated by logwatch at {escape(_now_stamp())} '
        f'&middot; <span class="badge risk-{risk}">RISK: {risk}</span></p>',
        body,
        "<footer>Generated by logwatch &middot; zero-dependency log analysis &amp; "
        "security reporting. Analyse only logs you own or are authorised to "
        "inspect.</footer>",
        "</div></body></html>",
    ]
    return "\n".join(parts) + "\n"


def _html_access_body(data: Dict, top_n: int) -> str:
    span = data["time_span"]
    peak = data["peak_minute"]
    out: List[str] = ["<h2>Overview</h2>"]
    out.append(
        _h_table(
            ["Metric", "Value"],
            [
                ("Lines read", data["files_lines"]),
                ("Parsed requests", data["parsed"]),
                ("Unparsed lines", data["unparsed"]),
                ("Unique client IPs", data["unique_ips"]),
                ("Total bytes sent", _human_bytes(data["total_bytes"])),
                ("Time span", f"{span['first']} -> {span['last']}"),
                (
                    "Peak req/min",
                    f"{peak['count']} at {peak['minute']}" if peak["count"] else "n/a",
                ),
            ],
        )
    )

    out.append("<h2>Status codes</h2>")
    out.append(
        _h_table(
            ["Class", "Count"],
            list(data["status_classes"].items()),
            num_cols=(1,),
        )
    )

    out.append(f"<h2>Top {top_n} client IPs</h2>")
    out.append(_h_table(["IP", "Requests"], data["top_ips"], num_cols=(1,), mono_cols=(0,)))
    out.append(_h_disclaimer())

    out.append(f"<h2>Top {top_n} paths</h2>")
    out.append(_h_table(["Path", "Hits"], data["top_paths"], num_cols=(1,), mono_cols=(0,)))

    out.append(f"<h2>Top {top_n} user-agents</h2>")
    out.append(_h_table(["User-Agent", "Hits"], data["top_agents"], num_cols=(1,)))

    susp = data["suspicious"]
    out.append(f"<h2>Suspicious requests ({escape(str(susp['total']))})</h2>")
    if susp.get("suppressed"):
        out.append(
            f'<p class="note">{escape(str(susp["suppressed"]))} request(s) from '
            "allow-listed sources were suppressed.</p>"
        )
    if susp["total"] == 0:
        out.append('<p class="empty">No suspicious requests detected.</p>')
    else:
        out.append("<h3>By category</h3>")
        out.append(
            _h_table(
                ["Category", "Count"],
                [(_CATEGORY_LABELS.get(c, c), n) for c, n in susp["by_category"]],
                num_cols=(1,),
            )
        )
        out.append("<h3>Offending IPs</h3>")
        out.append(_h_table(["IP", "Flags"], susp["by_ip"], num_cols=(1,), mono_cols=(0,)))
        out.append(_h_disclaimer())
        out.append("<h3>Sample flagged requests</h3>")
        out.append(
            _h_table(
                ["IP", "Method", "Path", "Why"],
                [
                    (s["ip"], s["method"], s["path"], ", ".join(s["categories"]))
                    for s in susp["samples"]
                ],
                mono_cols=(0, 2),
            )
        )
    return "\n".join(out)


def _html_auth_body(data: Dict, top_n: int) -> str:
    span = data["time_span"]
    out: List[str] = ["<h2>Overview</h2>"]
    out.append(
        _h_table(
            ["Metric", "Value"],
            [
                ("Lines read", data["files_lines"]),
                ("Parsed events", data["parsed"]),
                ("Unparsed lines", data["unparsed"]),
                ("Failed logins", data["failed_logins"]),
                ("Accepted logins", data["accepted_logins"]),
                ("Time span", f"{span['first']} -> {span['last']}"),
            ],
        )
    )

    bf = data["bruteforce"]
    out.append(
        f"<h2>Brute-force suspects (&ge; {escape(str(bf['threshold']))} fails / "
        f"{escape(str(bf['window_minutes']))} min)</h2>"
    )
    if not bf["hits"]:
        out.append('<p class="empty">No brute-force activity detected.</p>')
    else:
        out.append(
            _h_table(
                ["IP", "Fails", "Peak/win", "Scope", "Users tried"],
                [
                    (
                        h["ip"],
                        h["failed_count"],
                        h["peak_in_window"],
                        h.get("scope", "other"),
                        ", ".join(h["users_tried"]),
                    )
                    for h in bf["hits"]
                ],
                num_cols=(1, 2),
                mono_cols=(0,),
            )
        )
        if any(h.get("scope") == "private" for h in bf["hits"]):
            out.append(
                '<p class="note"><strong>Note:</strong> one or more attacks '
                "originate from a PRIVATE address - possible lateral movement / "
                "internal host.</p>"
            )
        out.append(_h_disclaimer())

    out.append(f"<h2>Top {top_n} attacking IPs</h2>")
    out.append(
        _h_table(["IP", "Failures"], data["top_attacking_ips"], num_cols=(1,), mono_cols=(0,))
    )

    out.append(f"<h2>Top {top_n} invalid users tried</h2>")
    out.append(_h_table(["Username", "Attempts"], data["top_invalid_users"], num_cols=(1,)))

    out.append("<h2>Accepted logins</h2>")
    out.append(_h_table(["Username", "Logins"], data["accepted_users"], num_cols=(1,)))
    return "\n".join(out)
