"""Export an analyser result dict as a standalone HTML or Markdown report.

The terminal renderer in :mod:`logwatch.report` is for a TTY; these exporters
produce artefacts you keep or share:

* :func:`render_html` - a single self-contained ``.html`` file (inline CSS, no
  external assets, no JavaScript) that you can email, attach to a ticket or
  open from a file share. Opens fine offline.
* :func:`render_markdown` - a ``.md`` document that drops cleanly into a GitHub
  issue, a wiki or a CI job summary.

Both take the same plain dict produced by :mod:`logwatch.analyzers` (the very
dict that ``--json`` prints), so they stay in lock-step with the rest of the
tool and remain pure string builders with no I/O. Everything here is standard
library only (``html.escape`` for safe HTML, plain f-strings for Markdown).

Security note: log fields (paths, user-agents, usernames) are attacker-
controlled, so every value placed into the HTML is run through
:func:`html.escape`. That is what makes it safe to open a report built from a
hostile log in a browser.
"""

from __future__ import annotations

from datetime import datetime, timezone
from html import escape
from typing import Dict, List, Optional, Sequence

# Human-friendly names for the suspicious-request categories (kept in sync with
# report.py; duplicated rather than imported to keep these modules decoupled).
_CATEGORY_LABELS = {
    "path_traversal": "Path traversal (../)",
    "sqli": "SQL injection pattern",
    "xss": "Cross-site scripting probe",
    "scanner_path": "Scanner / recon path",
    "sensitive_file": "Sensitive-file probe",
    "oversized_query": "Oversized query string",
    "bad_method": "Unusual HTTP method",
}

# The disclaimer printed near IP tables in every export: logwatch is GeoIP-free
# on purpose (zero dependencies, no bundled database, no network lookups), so it
# must not pretend to know an address's owner, ASN or country.
ASN_DISCLAIMER = (
    "logwatch does not resolve IP ownership, ASN or geolocation - it ships with "
    "no GeoIP/ASN database and makes no network calls. To attribute an address, "
    "look it up yourself (e.g. an RDAP/whois query such as "
    "`whois <ip>`, or your provider's threat-intel feed)."
)


def _human_bytes(num: int) -> str:
    """Format a byte count as a compact human string (e.g. 12.3 MB)."""
    value = float(num)
    for unit in ("B", "KB", "MB", "GB", "TB"):
        if value < 1024 or unit == "TB":
            if unit == "B":
                return f"{int(value)} {unit}"
            return f"{value:.1f} {unit}"
        value /= 1024
    return f"{num} B"


def _now_stamp() -> str:
    """Return a UTC timestamp for the 'generated at' line."""
    return datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M:%S UTC")


# ===========================================================================
# Markdown
# ===========================================================================

def _md_table(headers: Sequence[str], rows: Sequence[Sequence[object]]) -> List[str]:
    """Render a GitHub-flavoured Markdown table as a list of lines."""
    if not rows:
        return ["_(none)_", ""]
    out = ["| " + " | ".join(headers) + " |"]
    out.append("| " + " | ".join("---" for _ in headers) + " |")
    for row in rows:
        cells = [str(c).replace("|", "\\|") for c in row]
        out.append("| " + " | ".join(cells) + " |")
    out.append("")
    return out


def render_markdown(data: Dict, top_n: int) -> str:
    """Build a Markdown report from an analyser result dict."""
    if data.get("kind") == "auth":
        return _markdown_auth(data, top_n)
    return _markdown_access(data, top_n)


def _md_common_header(data: Dict, title: str) -> List[str]:
    risk = data.get("risk", "LOW")
    emoji = {"LOW": "🟢", "MEDIUM": "🟡", "HIGH": "🔴"}.get(risk, "⚪")
    out = [
        f"# {title}",
        "",
        f"_Generated by logwatch at {_now_stamp()}._",
        "",
        f"**Risk: {emoji} {risk}**",
        "",
    ]
    return out


def _markdown_access(data: Dict, top_n: int) -> str:
    out = _md_common_header(data, "HTTP access log report")

    span = data["time_span"]
    out += ["## Overview", ""]
    out += _md_table(
        ["Metric", "Value"],
        [
            ("Lines read", data["files_lines"]),
            ("Parsed requests", data["parsed"]),
            ("Unparsed lines", data["unparsed"]),
            ("Unique client IPs", data["unique_ips"]),
            ("Total bytes sent", _human_bytes(data["total_bytes"])),
            ("Time span", f"{span['first']} -> {span['last']}"),
            (
                "Peak req/min",
                f"{data['peak_minute']['count']} at {data['peak_minute']['minute']}"
                if data["peak_minute"]["count"]
                else "n/a",
            ),
        ],
    )

    out += ["## Status codes", ""]
    out += _md_table(
        ["Class", "Count"],
        [(k, v) for k, v in data["status_classes"].items()],
    )

    out += [f"## Top {top_n} client IPs", ""]
    out += _md_table(["IP", "Requests"], data["top_ips"])
    out += [f"_{ASN_DISCLAIMER}_", ""]

    out += [f"## Top {top_n} paths", ""]
    out += _md_table(["Path", "Hits"], data["top_paths"])

    out += [f"## Top {top_n} user-agents", ""]
    out += _md_table(["User-Agent", "Hits"], data["top_agents"])

    susp = data["suspicious"]
    out += [f"## Suspicious requests ({susp['total']})", ""]
    if susp.get("suppressed"):
        out += [
            f"_{susp['suppressed']} request(s) from allow-listed sources were "
            "suppressed._",
            "",
        ]
    if susp["total"] == 0:
        out += ["No suspicious requests detected.", ""]
    else:
        out += ["### By category", ""]
        out += _md_table(
            ["Category", "Count"],
            [(_CATEGORY_LABELS.get(c, c), n) for c, n in susp["by_category"]],
        )
        out += ["### Offending IPs", ""]
        out += _md_table(["IP", "Flags"], susp["by_ip"])
        out += [f"_{ASN_DISCLAIMER}_", ""]
        out += ["### Sample flagged requests", ""]
        out += _md_table(
            ["IP", "Method", "Path", "Why"],
            [
                (s["ip"], s["method"], s["path"], ", ".join(s["categories"]))
                for s in susp["samples"]
            ],
        )

    return "\n".join(out).rstrip() + "\n"


def _markdown_auth(data: Dict, top_n: int) -> str:
    out = _md_common_header(data, "SSH auth log report")

    span = data["time_span"]
    out += ["## Overview", ""]
    out += _md_table(
        ["Metric", "Value"],
        [
            ("Lines read", data["files_lines"]),
            ("Parsed events", data["parsed"]),
            ("Unparsed lines", data["unparsed"]),
            ("Failed logins", data["failed_logins"]),
            ("Accepted logins", data["accepted_logins"]),
            ("Time span", f"{span['first']} -> {span['last']}"),
        ],
    )

    bf = data["bruteforce"]
    out += [
        f"## Brute-force suspects (>= {bf['threshold']} fails / "
        f"{bf['window_minutes']} min)",
        "",
    ]
    if not bf["hits"]:
        out += ["No brute-force activity detected.", ""]
    else:
        out += _md_table(
            ["IP", "Fails", "Peak/win", "Scope", "Users tried"],
            [
                (
                    h["ip"],
                    h["failed_count"],
                    h["peak_in_window"],
                    h.get("scope", "other"),
                    ", ".join(h["users_tried"]),
                )
                for h in bf["hits"]
            ],
        )
        if any(h.get("scope") == "private" for h in bf["hits"]):
            out += [
                "> **Note:** one or more attacks originate from a PRIVATE "
                "address - possible lateral movement / internal host.",
                "",
            ]
        out += [f"_{ASN_DISCLAIMER}_", ""]

    out += [f"## Top {top_n} attacking IPs", ""]
    out += _md_table(["IP", "Failures"], data["top_attacking_ips"])

    out += [f"## Top {top_n} invalid users tried", ""]
    out += _md_table(["Username", "Attempts"], data["top_invalid_users"])

    out += ["## Accepted logins", ""]
    out += _md_table(["Username", "Logins"], data["accepted_users"])

    return "\n".join(out).rstrip() + "\n"


